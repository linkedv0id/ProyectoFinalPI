    private void jbModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbModificarActionPerformed
        // TODO add your handling code here:
        if(jbModificar.getText().equalsIgnoreCase("Modificar")){
            if(jTable1.getSelectedRow() == -1){
               if(jTable1.getRowCount() == 0){
                   JOptionPane.showMessageDialog(this,"No hay registros");
               }
               else{
                   JOptionPane.showMessageDialog(this,"Seleccione una fila");
               }
               } else{        
               txtNombre.setEnabled(true);               
               jbBorrar.setEnabled(false);
               jbNuevo.setEnabled(false);
               jbRegistrar.setEnabled(false);
               jbModificar.setText("Actualizar");
               jbCancelar.setEnabled(true);
               jTable1.setEnabled(false);
           }} else {
            
             txtNombre.setEnabled(false);             
             jbBorrar.setEnabled(true);
             jbNuevo.setEnabled(true);
             jbRegistrar.setEnabled(false);
             jbModificar.setText("Modificar");
             jbCancelar.setEnabled(false);
             jTable1.setEnabled(true);
             
            //Se crea el objeto categoria 
             Categoria categoria = new Categoria();
             //Se configura los datos en el objeto programa de la clase
             //Programa
             categoria.setID(Integer.parseInt(txtID.getText()));
             categoria.setNombre(txtNombre.getText());                                        
               
                         
             if(ControladorCategoria.modificarCategoria(categoria) == 1){
                 JOptionPane.showMessageDialog(this,"Actualización exitosa");
                 this.cargarCategorias();
             } else {
                 JOptionPane.showMessageDialog(this,"Actualización Fallida");
             }
             
        }
             
    }//GEN-LAST:event_jbModificarActionPerformed
    private void cargarCategorias(){
        DefaultTableModel modelo;
        modelo = (DefaultTableModel) jTable1.getModel();
        ArrayList <Categoria> listadoCategorias =new ArrayList();
        listadoCategorias=ControladorCategoria.listadoCategorias("0");
        for(int i= 0; i < listadoCategorias.size(); i++){                       
              modelo.addRow(new Object[]{
              listadoCategorias.get(i).getID(),
              listadoCategorias.get(i).getNombre(),
                 
              });
        }      
    }

